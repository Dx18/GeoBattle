package geobattle.geobattle.util;

// Rectangle with int coordinates
public final class IntRect {
    // X coordinate
    public int x;

    // Y coordinate
    public int y;

    // Width of rectangle
    public int width;

    // Height of rectangle
    public int height;

    public IntRect(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
